export default function StickyHeadTable() {
    const [data,setData]=React.useState([])
  const [page, setPage] = React.useState(0);
  const [rowsPerPage, setRowsPerPage] = React.useState(10);

  const handleChangePage = (event, newPage) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (event) => {
    setRowsPerPage(+event.target.value);
    setPage(0);
  };

  React.useEffect(()=>{

    fetch('http://localhost:8080/transactions')
    .then((res)=>{
      return res.json();
    })
    .then((json)=>{
      console.log(json);
      setData(json);
      
      
    })
       
      

   },[])

  return <>
    <div styles={{width:'80%',padding:"30px",paddingTop:"40px",margin:"auto"}}>
  <div style={{marginTop:"50px",marginBottom:"30px"}}>
  <TextField
          required
          id="outlined-required"
          label="Required"
          defaultValue="Select Start Date"
        />
        <TextField
          
          id="outlined-disabled"
          label="Required"
          defaultValue="Select End Date"
        />
        <TextField
          id="outlined-password-input"
          label="From"
          type="text"
          autoComplete="From"
        />
         <TextField
          id="outlined-password-input"
          label="To"
          type="text"
          autoComplete="To"
        />
  </div>
    <Paper sx={{ width: '100%', overflow: 'hidden' }}>
      <TableContainer sx={{ maxHeight: 440 }}>
        <Table stickyHeader aria-label="sticky table">
          <TableHead>
          <TableRow>
            <StyledTableCell>Id</StyledTableCell>
            <StyledTableCell align="left">Machine</StyledTableCell>
            <StyledTableCell align="left">Command</StyledTableCell>
            <StyledTableCell align="left">P1</StyledTableCell>
        
          </TableRow>
          </TableHead>
          <TableBody>
            {data
              .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
              .map((row) => {
                return (
                  <TableRow hover role="checkbox" tabIndex={-1} key={row.code}>
                    {columns.map((column) => {
                      const value = row[column.id];
                      return (
                        <TableCell key={column.id} align={column.align}>
                          {column.format && typeof value === 'number'
                            ? column.format(value)
                            : value}
                        </TableCell>
                      );
                    })}
                  </TableRow>
                );
              })}
          </TableBody>
        </Table>
      </TableContainer>
      <TablePagination
        rowsPerPageOptions={[10, 25, 100]}
        component="div"
        count={rows.length}
        rowsPerPage={rowsPerPage}
        page={page}
        onPageChange={handleChangePage}
        onRowsPerPageChange={handleChangeRowsPerPage}
      />
    </Paper>
    </div>
    </>
}